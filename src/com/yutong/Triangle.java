package com.yutong;

public class Triangle {

	private int edge1;
	private int edge2;
	private int edge3;

	public Triangle(int edge1, int edge2, int edge3) {
		this.edge1 = edge1;
		this.edge2 = edge2;
		this.edge3 = edge3;
	}

	public int getEdge1() {
		return this.edge1;
	}

	public int getEdge2() {
		return this.edge2;
	}

	public int getEdge3() {
		return this.edge3;
	}
}
